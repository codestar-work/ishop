package web;

public class Product {
	long code = 0;
	String name = "";
	String detail = "";
	String photo = "";
	double price = 0.0;
	public long getCode()     { return code;   }
	public String getName()   { return name;   }
	public String getDetail() { return detail; }
	public String getPhoto()  { return photo;  }
	public double getPrice()  { return price;  }
}